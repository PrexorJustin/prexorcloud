// Banner returns the multi-line PrexorCloud wordmark in brand color.
func Banner() string {
	if theme.ASCII {
		return theme.Heading("PREXORCLOUD")
	}

	brand := theme.FG(theme.Brand)
	glow := theme.FG(theme.Magenta)

	var b strings.Builder
	for i, line := range bannerLines {
		if i == len(bannerLines)-1 {
			b.WriteString(glow.Render(line))
		} else {
			b.WriteString(brand.Render(line))
		}
		b.WriteString("\n")
	}
	return b.String()
}
